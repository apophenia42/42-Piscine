int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int b;

	b = 0;
	while (*s1 && *s2 && b < n)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
		b++;
	}
	s1--;
	s2--;
	return (*s1 - *s2);
}
