int	ft_is_prime(int nb)
{
	int n;
	int flag;

	n = nb - 1;
	flag = 1;
	while (n-- > 2)
	{
		if (nb % n == 0)
			flag = 0;
	}
	if (nb <= 1)
		flag = 0;
	return (flag);
}
