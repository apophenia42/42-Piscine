void	ft_putchar(char c);

void	ft_print_comb2(void)
{
	char n[3];

	n[0] = '0';
	while (n[0] <= '9')
	{
		n[1] = '0';
		while (n[1] <= '9')
		{
			n[2] = '0';
			while (n[2] <= '9')
			{
				n[3] = 1 + n[1];
				while (n[3] <= '9')
				{
					ft_putchar(n[0]);
					ft_putchar(n[1]);
					ft_putchar(' ');
					ft_putchar(n[2]);
					ft_putchar(n[3]);
					ft_putchar(',');
					ft_putchar(' ');
					n[3]++;
				}
				n[2]++;
			}
			n[1]++;
		}
		n[0]++;
	}
}
